import UIKit

extension UIFont {

    class func helveticaNeue(ofSize: CGFloat) -> UIFont {
        return UIFont.init(name: "HelveticaNeue", size: ofSize)!
    }
    
    class func helveticaNeueBold(ofSize: CGFloat) -> UIFont {
        return UIFont.init(name: "HelveticaNeue-Bold", size: ofSize)!
    }
    
    class func helveticaNeueCondencedBold(ofSize: CGFloat) -> UIFont {
        return UIFont.init(name: "HelveticaNeue-CondensedBold", size: ofSize)!
    }



}
